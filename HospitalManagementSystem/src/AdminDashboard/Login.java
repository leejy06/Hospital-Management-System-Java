    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed

    javax.swing.JFrame next = null;

String Uid;
        String Upass;
        Uid = user_id.getText();
        Upass = user_password.getText();
        if (Uid.equals("admin") && Upass.equals("1234"))
        { 
             Admin myMain = new Admin();
             myMain.setVisible(true);
             myMain.setLocationRelativeTo(null);
             this.dispose();
        }
        else if (Uid.equals("doctor") && Upass.equals("1234"))
        { 
             DoctorDashboard.Appointment myMain = new DoctorDashboard.Appointment();
             myMain.setVisible(true);
             myMain.setLocationRelativeTo(null);
             this.dispose();
        }
        else if (Uid.equals("patient") && Upass.equals("1234"))
        { 
             PatientDashboard.Patient myMain = new PatientDashboard.Patient();
             myMain.setVisible(true);
             myMain.setLocationRelativeTo(null);
             this.dispose();
        }
        else
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid username or password.");
            user_id.setText("");
            user_password.setText("");
            user_id.requestFocusInWindow();
        }

    }//GEN-LAST:event_loginActionPerformed

